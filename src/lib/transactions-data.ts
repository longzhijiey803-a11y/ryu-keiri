export function filterTransactions(
  list: Transaction[],
  f: TransactionFilter,
): Transaction[] {
  const q = f.query.trim().toLowerCase();
  return list.filter((t) => {
    if (f.kind !== "all" && t.kind !== f.kind) return false;
    if (f.status !== "all" && t.status !== f.status) return false;
    if (f.assignee_id !== "all" && t.assignee_id !== f.assignee_id)
      return false;
    if (q) {
      const hay = `${t.id} ${t.name} ${t.partner.name}`.toLowerCase();
      if (!hay.includes(q)) return false;
    }
    return true;
  });
}

export function groupByStatus(
  list: Transaction[],
): Record<TransactionStatus, Transaction[]> {
  const acc = {
    draft: [],
    review: [],
    approval: [],
    scheduled_payment: [],
    awaiting_deposit: [],
    done: [],
    rejected: [],
  } as Record<TransactionStatus, Transaction[]>;
  for (const t of list) acc[t.status].push(t);
  return acc;
}
